package sjsu.team21.beans;

public class WaterFallPhaseStatus {

	float Requirement;
	float Design;
	float Implementation;
	float Testing;
	float Maintenance;
	public float getRequirement() {
		return Requirement;
	}
	public void setRequirement(float requirement) {
		Requirement = requirement;
	}
	public float getDesign() {
		return Design;
	}
	public void setDesign(float design) {
		Design = design;
	}
	public float getImplementation() {
		return Implementation;
	}
	public void setImplementation(float implementation) {
		Implementation = implementation;
	}
	public float getTesting() {
		return Testing;
	}
	public void setTesting(float testing) {
		Testing = testing;
	}
	public float getMaintenance() {
		return Maintenance;
	}
	public void setMaintenance(float maintenance) {
		Maintenance = maintenance;
	}
	
	
}
