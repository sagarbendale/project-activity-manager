package sjsu.team21.beans;

public class KanbanStatus {

	float InProgress;
	float Requested;
	float Completed;
	public float getInProgress() {
		return InProgress;
	}
	public void setInProgress(float inProgress) {
		InProgress = inProgress;
	}
	public float getRequested() {
		return Requested;
	}
	public void setRequested(float requested) {
		Requested = requested;
	}
	public float getCompleted() {
		return Completed;
	}
	public void setCompleted(float completed) {
		Completed = completed;
	}
	
	
}
